#include "3-calc.h"
/**
 * op_add - function to sum two integers
 * Return: sum of a and b
 * @a: integer
 * @b: integer
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction of two integers
 * Return: diference between a and b
 * @a: integer
 * @b: integer
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplicates two numbers
 * Return: a multiplicated for b
 * @a: integer
 * @b: integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - functiont that divides a by b
 * Return: a divided by b
 * @a: integer
 * @b: integer
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * Return: a % b
 * @a: integer
 * @b: integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}
